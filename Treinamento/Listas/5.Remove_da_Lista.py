#Remoção de elemento com base no seu valor

programadores = ['Victor', 'Juliana', 'Samuel', 'Caio', 'Luana']						# Declaração da lista
print(programadores)  																	# Imprimindo a lista original
programadores.remove('Caio')  															# Busca o elemento "Caio" na lista e o remove
																						# Caso "Caio" esteja repetido na lista, apenas  
																						# sua primeira aparição será removida

print(programadores)  																	# Imprimindo lista alterada
