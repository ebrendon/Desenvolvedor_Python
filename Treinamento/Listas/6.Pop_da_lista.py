# Remoção de elemento com base no seu índice

programadores = ['Victor', 'Juliana', 'Samuel', 'Caio', 'Luana']				# Declaração da lista
print(programadores)  															# Imprimindo a lista original
programadores.pop(2)  															# Remove o índice 2 da lista
																				# Caso pop() esteja com o parâmetro vazio,
																				# ele remove o último elemento da lista

																				
print(programadores)  															# Imprimindo lista alterada
